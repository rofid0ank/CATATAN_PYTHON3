# if, elif, else

# if
angka = 3
if angka > 0:
  print('benar, angka 3 lebih besar dari angka 0')

# if, else
nama = 'rofi'
if nama == nama:
  print('benar, itu saya')
else:
  print('salah, itu bukan saya')

# if, elif, else
nilai = 80
if nilai > 80:
  print('anda tidak mengikuti remedial')
elif nilai == 80:
  print('anda tidak mengikuti remedial')
else:
  print('anda harus mengikuti remedial')
